var kayitVeri = localStorage.getItem("ingilizceVeri")
let diziSira = kayitVeri || 0
let harfSira = 0
let solUstKelime = ""
canvas.width = window.innerWidth
canvas.height = window.innerHeight
const ballRadius = 15
let ballX = canvas.width / 2
let ballY = canvas.height / 2
const joystick = document.getElementById("joystick")
const joystickInner = document.getElementById("joystickInner")
let isMoving = false
let currentOffsetX = 0
let currentOffsetY = 0
let firstName = veriDizi[diziSira].split(" / ")[0].split("")
let anlamKelime = veriDizi[diziSira].split(" / ")[1]
let blueBalls = []

function drawName(name) {
    ctx.fillStyle = "black"
    ctx.font = "bold 24px Arial"
    ctx.textAlign = "left"
    ctx.letterSpacing = "2px"
    ctx.textBaseline = "top"
    ctx.fillText(name, 10, 10)
}
function drawAnlam(anlam) {
    ctx.fillStyle = "#18181a"
    ctx.font = "bold 24px Arial"
    ctx.textAlign = "left"
    ctx.textBaseline = "top"
    ctx.fillText(anlam, 10, 40)
}

function drawName2(name2) {
    ctx.fillStyle = "white"
    ctx.font = "bold 24px Arial"
    ctx.textAlign = "left"
    ctx.textBaseline = "top"   
    ctx.fillText(name2, 10, 10)
}

function anlikKelime(veri) {
    ctx.fillStyle = "white"
    ctx.font = "bold 24px Arial"
    ctx.textAlign = "left"
    ctx.textBaseline = "top"  
    ctx.fillText(veri, 10, canvas.height - 50)
}

function createBlueBalls() {
    const radius = 30
    const positions = new Set()

    firstName.forEach((letter) => {
        let x, y
        let validPosition = false

        while (!validPosition) {
            x = Math.random() * (canvas.width / 1.1 - radius * 2) + radius
            y = Math.random() * (canvas.height / 1.25 - radius * 2) + radius

            validPosition = true
            for (const ball of blueBalls) {
                const distanceToBallCenter = Math.sqrt((ball.x - x) ** 2 + (ball.y - y) ** 2)
                if (distanceToBallCenter < ball.radius + radius) {
                    validPosition = false
                    break
                }
            }
        }
        blueBalls.push({
            x: x,
            y: y,
            letter: letter,
            radius: radius,
        })
    })
}

joystick.addEventListener('touchstart', (event) => {
    isMoving = true
    event.preventDefault()
})

document.addEventListener('touchend', () => {
    isMoving = false
    joystickInner.style.transform = 'translate(0, 0)'
})

joystick.addEventListener('mousedown', (event) => {
    isMoving = true
})

document.addEventListener('mouseup', () => {
    isMoving = false
    joystickInner.style.transform = 'translate(0, 0)'
})

function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height)
    drawBlueBalls()
    drawBall()
    drawName(veriDizi[diziSira].split(" / ")[0])
    drawAnlam(anlamKelime)
    drawName2(solUstKelime)
    anlikKelime("3650 / " + (Number(diziSira)+1))
    moveBall(currentOffsetX, currentOffsetY)
    requestAnimationFrame(draw)
}

function drawBall() {
    ctx.beginPath()
    ctx.arc(ballX, ballY, ballRadius, 0, Math.PI * 2)
    ctx.fillStyle = "white"
    ctx.fill()
    ctx.closePath()
}

function drawBlueBalls() {
    blueBalls.forEach(ball => {
        ctx.beginPath()
        ctx.arc(ball.x, ball.y, ball.radius, 0, Math.PI * 2)
        ctx.fillStyle = "#242627"
        ctx.fill()
        ctx.closePath()
        ctx.fillStyle = "white"
        ctx.font = "bold 24px Arial"
        ctx.textAlign = "center"
        ctx.textBaseline = "middle"
        ctx.fillText(ball.letter, ball.x, ball.y)
    })
}

joystick.addEventListener('touchmove', (event) => {
    if (isMoving) {
        const rect = joystick.getBoundingClientRect()
        const touch = event.touches[0]
        const offsetX = touch.clientX - (rect.left + rect.width / 2)
        const offsetY = touch.clientY - (rect.top + rect.height / 2)
        const distance = Math.sqrt(offsetX * offsetX + offsetY * offsetY)
        
        if (distance > (rect.width / 2)) { 
            const angle = Math.atan2(offsetY, offsetX)
            currentOffsetX = Math.cos(angle) * (rect.width / 2)
            currentOffsetY = Math.sin(angle) * (rect.height / 2)
        } else {
            currentOffsetX = offsetX
            currentOffsetY = offsetY
        }

        moveBall(currentOffsetX, currentOffsetY)
        joystickInner.style.transform = `translate(${currentOffsetX}px, ${currentOffsetY}px)`
    }
})

document.addEventListener('mousemove', (event) => {
    if (isMoving) { 
        const rect = joystick.getBoundingClientRect()
        const offsetX = event.clientX - (rect.left + rect.width / 2)
        const offsetY = event.clientY - (rect.top + rect.height / 2)
        const distance = Math.sqrt(offsetX * offsetX + offsetY * offsetY)
        if (distance > (rect.width / 2)) { 
            const angle = Math.atan2(offsetY, offsetX)
            currentOffsetX = Math.cos(angle) * (rect.width / 2)
            currentOffsetY = Math.sin(angle) * (rect.height / 2)
        } else {
            currentOffsetX = offsetX
            currentOffsetY = offsetY
        }
        moveBall(currentOffsetX, currentOffsetY)
        joystickInner.style.transform = `translate(${currentOffsetX}px, ${currentOffsetY}px)`
    } else if(isMoving && joystick.contains(event.target)) { 
       moveBall(currentOffsetX, currentOffsetY)     
   }
})

function moveBall(offsetX, offsetY) {
    if(isMoving){
        ballX += offsetX * 0.05
        if (ballX + ballRadius > canvas.width) ballX = canvas.width - ballRadius
        if (ballX - ballRadius < 0) ballX = ballRadius
        ballY += offsetY * 0.05
        if (ballY + ballRadius > canvas.height) ballY = canvas.height - ballRadius
        if (ballY - ballRadius < 0) ballY = ballRadius
        checkCollision()
    } 
}

function checkCollision() {
    blueBalls.forEach(ball => { 
        const distanceToBallCenter = Math.sqrt((ball.x - ballX) ** 2 + (ball.y - ballY) ** 2);    
        if (distanceToBallCenter < ball.radius + ballRadius) { 
            if (ball.letter == firstName[harfSira]) {
                solUstKelime += firstName[harfSira]
                blueBalls.splice(blueBalls.indexOf(ball), 1)
                harfSira++                
                if(harfSira == firstName.length && diziSira+1 != veriDizi.length){                   
                    blueBalls = []
                    diziSira++
                    localStorage.setItem("ingilizceVeri",Number(diziSira))
                    firstName = veriDizi[diziSira].split(" / ")[0].split("")
                    anlamKelime = veriDizi[diziSira].split(" / ")[1]
                    harfSira = 0
                    solUstKelime = ""
                    createBlueBalls()
                }
                if (harfSira == firstName.length && diziSira+1 ==veriDizi.length && window.location.host == "yapaygezegen.com") {
                    setTimeout(() => {                       
                        blueBalls = []
                        diziSira = 0
                        localStorage.setItem("ingilizceVeri",Number(diziSira))
                        firstName = veriDizi[diziSira].split(" / ")[0].split("")
                        anlamKelime = veriDizi[diziSira].split(" / ")[1]
                        harfSira = 0
                        solUstKelime = ""
                        createBlueBalls()
                    }, 5000);
                }
                return
            }   
        }
    })
}
